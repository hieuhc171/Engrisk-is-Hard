    private void OnLoginClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_OnLoginClicked
        // TODO add your handling code here:
        if(tfUsername.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Điền tên đăng nhập", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if(tfPassword.getText().isBlank()) {
            JOptionPane.showMessageDialog(this, "Điền mật khẩu", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }
        KetNoiCSDL();
        String query = "SELECT * FROM users";
        try {
            PreparedStatement stm = cnn.prepareStatement(query);
            ResultSet rs = stm.executeQuery();
            while(rs.next()) {
                if(tfUsername.getText().equals(rs.getString("Username")) && 
                   tfPassword.getText().equals(rs.getString("Password"))) {
                    JOptionPane.showMessageDialog(this, "Đăng nhập thành công!");

//                    User.Instance().setInfo(rs.getInt("UserID"),
//                                            tfUsername.getText(),
//                                            rs.getInt("Level"),
//                                            rs.getInt("Exp"));
                    OnSkipClick(evt);
                    return;
                }
            }
            JOptionPane.showMessageDialog(this, "Tên đăng nhập hoặc mật khẩu không chính xác!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
        catch(Exception ex) {
            java.util.logging.Logger.getLogger(FormLogin.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(this, "Lỗi SQL!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_OnLoginClicked

